// Опции для выпадающих списков «Тип заведения» и «Кухня» в форме нового запроса.
//
// Значения (value) должны совпадать с тегами в базе (CSV final_blyat_v3.csv),
// чтобы фильтр в place_search находил заведения. Бэкенд дополнительно делает
// мягкое сопоставление (по словам/через запятую) и фолбэк, но точные значения
// уменьшают долю пустых выдач.
//
// Если набор данных изменится — обнови списки ниже (источник: get_available_types /
// get_available_cuisines из backend/ms-v2/place_search.py).

export type SelectOption = { value: string; label: string };

const cap = (s: string): string => (s ? s.charAt(0).toUpperCase() + s.slice(1) : s);

const toOptions = (values: string[]): SelectOption[] =>
  values.map((v) => ({ value: v, label: cap(v) }));

// Типы заведений (канонические значения из базы, в нижнем регистре).
