-- ========== 3. RLS для доменных таблиц по run ==========
-- Примечание: ранее в проекте были политики "Allow all for anon".
-- Здесь добавляем политики для authenticated. Если хотите закрыть anon полностью — удалите старые политики вручную.

create or replace function public.can_access_run(rid bigint)
returns boolean
language sql
stable
as $$
  select exists (
    select 1
    from public.analysis_runs ar
    join public.client_requests cr on cr.id = ar.request_id
    where ar.id = rid and cr.user_id = auth.uid()
  );
$$;

