-- История запросов клиентов и запусков анализа (v2).
-- 1) client_requests: что запросил клиент
-- 2) analysis_runs: конкретный прогон (competitive/market) по запросу
-- 3) run_id + conclusion в доменных таблицах

-- ========== 1. Таблицы истории ==========

create table if not exists public.client_requests (
  id bigint generated by default as identity primary key,
  user_id uuid not null references auth.users(id) on delete cascade,
  query_text text not null default '',
  created_at timestamptz not null default now()
);

create table if not exists public.analysis_runs (
  id bigint generated by default as identity primary key,
  request_id bigint not null references public.client_requests(id) on delete cascade,
  report_type text not null default '', -- competitive | market
  source_csv text not null default '',
  top_n int not null default 0,
  perplexity_model text not null default '',
  created_at timestamptz not null default now()
);

create index if not exists idx_client_requests_user_id on public.client_requests(user_id);
create index if not exists idx_analysis_runs_request_id on public.analysis_runs(request_id);
create index if not exists idx_analysis_runs_created_at on public.analysis_runs(created_at);

alter table public.client_requests enable row level security;
alter table public.analysis_runs enable row level security;

-- Пользователь видит только свои запросы/запуски
drop policy if exists "Users can read own requests" on public.client_requests;
create policy "Users can read own requests"
  on public.client_requests for select
  using (auth.uid() = user_id);

drop policy if exists "Users can create own requests" on public.client_requests;
create policy "Users can create own requests"
  on public.client_requests for insert
  with check (auth.uid() = user_id);

drop policy if exists "Users can read own runs" on public.analysis_runs;
create policy "Users can read own runs"
  on public.analysis_runs for select
  using (
    exists (
      select 1
      from public.client_requests cr
      where cr.id = analysis_runs.request_id
        and cr.user_id = auth.uid()
    )
  );

